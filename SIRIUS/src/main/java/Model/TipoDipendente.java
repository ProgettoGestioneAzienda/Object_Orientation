package Model;

/**
 * L'enum TipoDipendente rappresenta i diversi livelli di anzianità o seniority
 * per i dipendenti in un'organizzazione. I tre possibili valori sono Junior, Middle e Senior.
 */
public enum TipoDipendente {

    /**
     * Rappresenta il livello di anzianità Junior per un dipendente.
     */
    Junior,

    /**
     * Rappresenta il livello di anzianità Middle per un dipendente.
     */
    Middle,

    /**
     * Rappresenta il livello di anzianità Senior per un dipendente.
     */
    Senior
}
